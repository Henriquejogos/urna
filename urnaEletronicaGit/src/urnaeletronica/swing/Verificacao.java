    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        clip.setFramePosition(0);
        clip.start();
        if(number1 == null || number2 == null || number3 == null || number4 == null || number5 == null){
            JOptionPane.showMessageDialog(rootPane, "Preencha todas os espa√ßos de digitos", "Erro",  JOptionPane.INFORMATION_MESSAGE);

        }else{
            try{
                Integer realInt1 = Integer.parseInt(number1);
                Integer realInt2 = Integer.parseInt(number2);
                Integer realInt3 = Integer.parseInt(number3);
                Integer realInt4 = Integer.parseInt(number4);
                Integer realInt5 = Integer.parseInt(number5);
                realInt1 = realInt1 * 10000;
                realInt2 = realInt2 * 1000;
                realInt3 = realInt3 * 100;
                realInt4 = realInt4 * 10;
                realInt5 = realInt5;
                Integer codigo = realInt1 + realInt2 + realInt3 + realInt4 + realInt5;
                
                EleicaoBO.conferirCodigo(codigo);
                Home.codigo = codigo;
                new Home().setVisible(true);
                dispose();

            }catch(BusinessException ex){
                JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Erro",  JOptionPane.INFORMATION_MESSAGE);
            } catch (UnsupportedAudioFileException ex) {
                Logger.getLogger(Verificacao.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Verificacao.class.getName()).log(Level.SEVERE, null, ex);
            } catch (LineUnavailableException ex) {
                Logger.getLogger(Verificacao.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton11ActionPerformed

